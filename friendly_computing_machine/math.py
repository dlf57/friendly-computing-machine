"""
A small set of functions for doing math operations.
"""

# Write a fucntion named add that adds two values

def add(arg1, arg2):
    """
    Function that adds two arguments
    """
    return arg1 + arg2

def mult(arg1, arg2):
    """
    Function that multiplies two arguments
    """
    return arg1 * arg2

def sub(arg1, arg2):
    """
    Function that subtracts two arguments
    """
    return arg1 - arg2

def div(arg1, arg2):
    """
    fucntion that divides tow arguments
    """
    return arg1 / arg2
